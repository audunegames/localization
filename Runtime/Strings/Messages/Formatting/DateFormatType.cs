namespace Audune.Localization
{
  /// <summary>
  /// Enum that defines the type of a message component that defines a date format argument.
  /// </summary>
  public enum DateFormatType
  {
    /// <summary>
    /// Format the argument as a date argument.
    /// </summary>
    Date,

    /// <summary>
    /// Format the argument as a time argument.
    /// </summary>
    Time,
  }
}