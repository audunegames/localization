namespace Audune.Localization
{
  /// <summary>
  /// Enum that defines the style of a message component that defines a date format argument.
  /// </summary>
  public enum DateFormatStyle
  {
    /// <summary>
    /// The short date format style, e.g. "6/15/2009" or "1:45 PM".
    /// </summary>
    Short,

    /// <summary>
    /// The long date format style, e.g. "Monday, June 15, 2009" or "1:45:30 PM".
    /// </summary>
    Long,
  }
}